//Handles the processing for the "*-MATRIX.TXT" file
void ProduceOutputFile4(string outFileName, vertex adj[20][20], int vCount, int adj2[20][20]){
	FILE* ptr;
	ptr=fopen(outFileName, "w");
	
	//Initializes the 2d int matrix into all zeroes
	for(int i=0; i<vCount; i++){
		for(int j=0; j<vCount; j++)
			adj2[i][j]=0;
	}
	
	vertex comp[vCount];
	//Copies the vertex IDs into a 1D array to be used for comparing
	for(int i=0; i<vCount; i++)
		strcpy(comp[i],adj[i][0]);
	/*
	Triple nested for loop for populating the the adjacency matrix.
	First loop iterates through the 1D comp matrix 
	Second loop iterates through the rows/vertices of the matrix/graph
	Third loop iterates through the edges of the current vertex
	*/
	for(int i=0; i<vCount; i++){
		for(int j=0; j<vCount; j++){
			for(int k=0; k<vCount; k++){
				if(strcmp(adj[j][k],comp[i])==0) 
					adj2[j][i]=1;				 
			}
		}
	}
	
	fprintf(ptr,"    ");
	for(int i=0; i<vCount; i++)
		fprintf(ptr,"%s ",comp[i]);
	fprintf(ptr,"\n");
		
	for(int i=0;i<vCount;i++){
		adj2[i][i]=0; //wipes the false positives generated when the ID's were compared to the vertices and not the edges
		fprintf(ptr,"%s ",adj[i][0]);
		for(int j=0;j<vCount;j++){
			fprintf(ptr,"%d ", adj2[i][j]);
		}
		fprintf(ptr,"\n");
	}
	fclose(ptr);
}

//Handles the processing for the "*-BFS.TXT" file
int ProduceOutputFile5(string outFileName, vertex adj[20][20], int vCount, int adj2[20][20]){
	FILE* ptr;
	
	vertex key;
	int index=-1; //int to store index of the chosen starting vertex to be passed on to DFS, assumed false
	
	printf("Input start vertex for the traversal: ");
	scanf("%s", key);
	
	for(int i=0; i<vCount; i++)	//for loop to locate the index of the starting index
		if(strcmp(key,adj[i][0])==0)
			index=i;

	if(index>-1){
		ptr=fopen(outFileName, "w"); 
		
		int visited[vCount];
		int queue[vCount];
	
		for(int i=0; i<vCount; i++){ //initializing arrays to track the vertices that have already been visited/marked
			visited[i]=0; //array to track vertices that have been visited
			queue[i]=-1;  //array to track vertices that have yet to be visited, initialized as -1 since this stores array indexes
		}
		 	
		BFS(adj, adj2, visited, queue, index, vCount, 1);
		
		for(int i=0; i<vCount; i++) //prints out the sequence in which vertices were visited
			if(queue[i]!=-1) //checker to insure it doesn't print values outside of the array
				fprintf(ptr,"%s ", adj[queue[i]][0]);
		
		fclose(ptr);
		return index; //returns the index of the chosen vertex to be passed on to DFS
	}
	else{ //if the starting vertex does not exist within the graph, forego opening/creating a file altogether and end the function
		printf("Vertex %s not found", key);
		return -1;
	}
}

