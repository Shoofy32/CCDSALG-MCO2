// Typedef declaration of custom string types to be used
typedef char vertex[9];
typedef char string[25];

// Handles the processing for the "*-SET.TXT" file
void ProduceOutputFile1(string outFileName, vertex adj[20][20], int vCount){

	FILE* ptr;
	int maxEdges;
	int cnt = 0; // Counter
	int edgeCount = 0; // Track how many edges are currently in the array

	ptr = fopen(outFileName, "w"); // Opens in write mode
	
	fprintf(ptr, "V(%c)={", outFileName[0]);

	for(int i = 0; i < vCount; i++)
		fprintf(ptr, "%s,", adj[i][0]);

	// Moves the cursor back by 1 space to overwrite the extra ',' character after loop has completed.
	fseek(ptr, -1, SEEK_CUR); 
	fprintf(ptr, "}\n");
	
	// Calculates the size of the edges array using the formula for a complete undirected graph
	maxEdges = vCount * (vCount-1) / 2;


	string edges[maxEdges];// Store all possible edges that can be generated by vertices
	vertex first[maxEdges]; // Store the first half of all possible edges that can be generated by vertices
	vertex second[maxEdges]; // Store the second half of all possible edges that can be generated by vertices
	string temp = "(";

	for(int i = 0; i < vCount; i++){

		for(int j = 1; j < vCount; j++){

			if(strcmp(adj[i][j], "\0")){

				// Checks if the edge already exists within the array
				if(checkEdge(adj[i][0], adj[i][j], first, second, maxEdges)){ 

					edgeCount++;
					
					strcpy(first[cnt],adj[i][0]); // Stores copy of the first half of the edge for later comparison
					strcpy(second[cnt],adj[i][j]); // Stores copy of the second half of the edge for later comparison
					
					strcat(temp,adj[i][0]); // Formats the two parts of the edge into one string before copying into the edges array
					strcat(temp,",");
					strcat(temp,adj[i][j]);
					strcat(temp,"),");
					
					strcpy(edges[cnt],temp);
					
					cnt++;

				}

				reformat(temp); //reformats the temp string
			}
		}
	}

	fprintf(ptr, "E(%c)={", outFileName[0]);
	
	for(int i = 0; i < edgeCount; i++)
		if(strcmp(edges[i], "\0"))
			fprintf(ptr, "%s", edges[i]);
	
	if(edgeCount)
		fseek(ptr,-1,SEEK_CUR); //Moves the cursor back by 1 space to overwrite the extra ',' character after loop has completed.
	
	fprintf(ptr, "}");
	fclose(ptr);

}

// Handles the processing for the "*-DEGREE.TXT" file
void ProduceOutputFile2(string outFileName, vertex adj[20][20], int vCount){

	FILE* ptr;
	ptr = fopen(outFileName, "w"); // Opens in write mode
	
	for(int i = 0; i < vCount; i++){

		int dCount = 0;

		for(int j = 1; j < vCount; j++)
			if(strcmp(adj[i][j],"\0")) //Checks whether element is empty 
				dCount++;
		
		fprintf(ptr,"%s %d\n", adj[i][0],dCount);
	}

	fclose(ptr);

}

